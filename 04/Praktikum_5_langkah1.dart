void main (){
  var record = ('first', a: 2, b: true, 'last');
print(record);
}
//Mengeluarkan output (first, last, a: 2, b: true)
/*
Tipe data record adalah tipe data untuk mengelompokkan beberapa nilai menjadi satu kesatuan tanpa harus membuat class atau struct. 
Record dapat memiliki field tanpa nama (positional) dan field bernama (named). Record berguna untuk mengembalikan beberapa nilai dari fungsi atau menyimpan data sementara secara ringkas.
*/